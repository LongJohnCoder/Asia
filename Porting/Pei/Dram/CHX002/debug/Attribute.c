 void StrReverse(CHAR16 *Str)
 
 {
 UINTN i,j;
 CHAR16 c;
 
	 for(i = 0, j = StrLen(Str) - 1; i < j; ++i, --j) {
 
		 c = *(Str+i);
 
		 *(Str+i) = *(Str+j) ;
 
		 *(Str+j)  = c;
 
	 }
 
 }


