	b3=b1 = ReadByte(cyc, offset, bus, dev, fun,  PciIndexPort,  PciDataPort) ;
    b1 = Get1Complementb(b1, mask);  
	WriteByte(cyc, offset, b1, bus, dev, fun,  PciIndexPort,  PciDataPort) ;     
    b2 = ReadByte(cyc, offset, bus, dev, fun,  PciIndexPort,  PciDataPort) ;     

