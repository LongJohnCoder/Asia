 const UINT8  ReadByte(CYCLE cyc, UINT64 offset, UINTN bus, UINTN dev, UINTN fun,UINTN  PciIndexPort, UINTN PciDataPort)
 {
	 UINT8 bRtn=0;
	 
	 switch(cyc)
	 {
		 case CFG: 
					 bRtn=AsiaPciRead8(PCI_LIB_ADDRESS(bus, dev, fun, offset));
					 break;
		 case MEM:				 
					 bRtn=AsiaMemoryRead8(offset);
					 break;
		 case IO:
					 if(!IsValidPort((UINT16)offset)){
						  bRtn = 0;
						 }
					 else{
						 bRtn = (UINT8)AsiaIoRead8((UINT16)offset);
						 }
					 break;

		 case LEVEL2:
		 			 bRtn=PciIOReadByte(bus, dev, fun,PciIndexPort,PciDataPort,(UINT32) offset);
					 break;

		 default:
					 break;
	 }
		return bRtn;
 }
 
 
